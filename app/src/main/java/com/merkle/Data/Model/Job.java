package com.merkle.Data.Model;

public class Job {
    String id;
    String type;
    String url;
    String created_at;
    String company;
    String company_url;
    String location;
    String title;
    String description;
    String how_to_apply;
    String company_logo;

    public String getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public String getUrl() {
        return url;
    }

    public String getCreated_at() {
        return created_at;
    }

    public String getCompany() {
        return company;
    }

    public String getCompany_url() {
        return company_url;
    }

    public String getLocation() {
        return location;
    }

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }

    public String getHow_to_apply() {
        return how_to_apply;
    }

    public String getCompany_logo() {
        return company_logo;
    }
}
